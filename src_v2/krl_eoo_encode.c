/* phi(x) = log((exp(x)+1)/(exp(x)-1))  (ldpc_decode.cpp: phi)
 *
 * 数値安定化のため log((1+e^-x)/(1-e^-x)) という数学的に等価な式で実装する。
 * 元のexpf(x)/(expf(x)-1)形式は x>~88 で expf(x)がfloatでオーバーフローして
 * +inf となり、inf/inf=NaN → log(NaN)=NaN が以後のBP全体に伝播して
 * 「全0符号語に偽収束する」不具合を引き起こす(実測: noise_var=0.0025程度の
 * 現実的なSNRでも |v|~400 となり発生する)。
 * e^-x 形式なら x>0 で常に (0,1] の範囲に収まり、大きなxではe^-x→0に
 * 安全にアンダーフローして phi(x)→0 (数学的に正しい極限) となる。 */
static float krl_eoo_phi(float x)
{
    float emx;
    if (x < 1e-10f) return KRL_EOO_LLR_MAX;
    emx = expf(-x); /* x>0 なので emx は (0,1) に収まり、オーバーフローしない */
    return logf((1.0f + emx) / (1.0f - emx));
}

/* 線形対数領域での log-sum-exp 近似 (ldpc_decode.cpp: max_star) */
static float krl_eoo_max_star(float x, float y)
{
    float maxXY = (x > y) ? x : y;
    float absdif = fabsf(x - y);
    float logXY = 0.0f;
    if (absdif <= 2.45f)      logXY = -0.24f * absdif + 0.596f;
    else if (absdif <= 3.5f)  logXY = 0.048f;
    return maxXY + logXY;
}

static float krl_eoo_clampf(float v, float lo, float hi)
{
    if (v < lo) return lo;
    if (v > hi) return hi;
    return v;
}

/* 1記号・1コンステレーション点の対数尤度 (ldpc_decode.cpp: calc_likelihood) */
static float krl_eoo_calc_likelihood(const RADE_COMP* sym, float var,
    float real, float imag,
    float amp, float avg_amp)
{
    float errR = sym->real - real;
    float errI = sym->imag - imag;
    float a = amp / avg_amp;
    return -var * a * a * (errR * errR + errI * errI);
}

/* Simplified-MAX-Log-MAP: 56 QPSK記号 → 112 チャネルLLR
 * (ldpc_decode.cpp: ldpc_linear_log_map) */
static void krl_eoo_linear_log_map(const RADE_COMP* syms, const float* amps,
    float noise_var, float* llr_out)
{
    int   k;
    float mean_amp = 0.0f;
    float EsNo;

    for (k = 0; k < KRL_EOO_INFO_BITS; k++) mean_amp += amps[k];
    mean_amp /= (float)KRL_EOO_INFO_BITS;
    EsNo = 1.0f / (2.0f * noise_var);

    for (k = 0; k < KRL_EOO_INFO_BITS; k++) {
        float a = amps[k];
        float num0, num1, den0, den1;
        float sl00, sl01, sl10, sl11;
        float llr_bit0, llr_bit1;

        sl00 = krl_eoo_calc_likelihood(&syms[k], EsNo, 1.0f, 0.0f, a, mean_amp); /* bits 00 */
        sl01 = krl_eoo_calc_likelihood(&syms[k], EsNo, 0.0f, 1.0f, a, mean_amp); /* bits 01 */
        sl10 = krl_eoo_calc_likelihood(&syms[k], EsNo, 0.0f, -1.0f, a, mean_amp); /* bits 10 */
        sl11 = krl_eoo_calc_likelihood(&syms[k], EsNo, -1.0f, 0.0f, a, mean_amp); /* bits 11 */

        num0 = num1 = den0 = den1 = -KRL_EOO_LLR_MAX;

        den0 = krl_eoo_max_star(den0, sl00);
        den1 = krl_eoo_max_star(den1, sl00);

        den0 = krl_eoo_max_star(den0, sl01);
        num1 = krl_eoo_max_star(num1, sl01);

        num0 = krl_eoo_max_star(num0, sl10);
        den1 = krl_eoo_max_star(den1, sl10);

        num0 = krl_eoo_max_star(num0, sl11);
        num1 = krl_eoo_max_star(num1, sl11);

        llr_bit0 = num0 - den0;
        llr_bit1 = num1 - den1;

        llr_out[2 * k] = -krl_eoo_clampf(llr_bit0, -KRL_EOO_LLR_MAX, KRL_EOO_LLR_MAX);
        llr_out[2 * k + 1] = -krl_eoo_clampf(llr_bit1, -KRL_EOO_LLR_MAX, KRL_EOO_LLR_MAX);
    }
}

/* LDPC(112,56) sum-product 復号結果 */
typedef struct {
    uint8_t message[KRL_EOO_CODE_BITS];
    int     converged;
    int     iterations;
} krl_eoo_ldpc_result_t;

/* sum-product BP復号 (ldpc_decode.cpp: ldpc_decode)
 * H行列 krl_eoo_hra から都度Tanner graphを構築する (E<=約280エッジ、
 * EOO受信毎=数秒に1回程度の頻度なので毎回構築しても負荷は無視できる)。 */
#define KRL_EOO_MAX_EDGES   320
#define KRL_EOO_MAX_CDEG      8   /* H行重み(実測4-5)に余裕を持たせた上限 */
#define KRL_EOO_MAX_VDEG      4   /* H列重み(実測1-3)に余裕を持たせた上限 */

static krl_eoo_ldpc_result_t krl_eoo_ldpc_decode(const RADE_COMP* syms,
    const float* amps,
    float noise_var,
    int max_iter)
{
    krl_eoo_ldpc_result_t result;
    float llr_ch[KRL_EOO_CODE_BITS];
    float m_vc[KRL_EOO_MAX_EDGES], m_cv[KRL_EOO_MAX_EDGES];
    int   edge_var[KRL_EOO_MAX_EDGES];
    int   check_edges[KRL_EOO_NUM_CHECKS][KRL_EOO_MAX_CDEG];
    int   check_edge_cnt[KRL_EOO_NUM_CHECKS];
    int   var_edges[KRL_EOO_CODE_BITS][KRL_EOO_MAX_VDEG];
    int   var_edge_cnt[KRL_EOO_CODE_BITS];
    int   E = 0;
    int   i, j, iter;

    memset(&result, 0, sizeof(result));
    memset(check_edge_cnt, 0, sizeof(check_edge_cnt));
    memset(var_edge_cnt, 0, sizeof(var_edge_cnt));

    /* --- Tanner graph 構築 (H行列の非零要素ごとに1エッジ) --- */
    for (i = 0; i < KRL_EOO_NUM_CHECKS; i++) {
        for (j = 0; j < KRL_EOO_CODE_BITS; j++) {
            if (krl_eoo_hra[i][j]) {
                if (E >= KRL_EOO_MAX_EDGES) break; /* 安全弁 (実測E=279) */
                edge_var[E] = j;
                if (check_edge_cnt[i] < KRL_EOO_MAX_CDEG) check_edges[i][check_edge_cnt[i]++] = E;
                if (var_edge_cnt[j] < KRL_EOO_MAX_VDEG)  var_edges[j][var_edge_cnt[j]++] = E;
                E++;
            }
        }
    }

    /* --- チャネルLLR --- */
    krl_eoo_linear_log_map(syms, amps, noise_var, llr_ch);

    for (i = 0; i < E; i++) {
        m_vc[i] = llr_ch[edge_var[i]];
        m_cv[i] = 0.0f;
    }

    for (iter = 0; iter < max_iter; iter++) {
        int ok;

        /* --- check-to-variable 更新 (tanh / sum-product則) --- */
        for (i = 0; i < KRL_EOO_NUM_CHECKS; i++) {
            int   nd = check_edge_cnt[i];
            int   sign = 1;
            float sum = 0.0f;
            int   k;

            for (k = 0; k < nd; k++) {
                float v = m_vc[check_edges[i][k]];
                sign *= (v >= 0.0f) ? 1 : -1;
                {
                    float p = krl_eoo_phi(fabsf(v));
                    sum += (p > 0.0f) ? p : 0.0f;
                }
            }
            for (k = 0; k < nd; k++) {
                int   e = check_edges[i][k];
                float v = m_vc[e];
                int   inv_sign = (v >= 0.0f) ? 1 : -1;
                float pv = krl_eoo_phi(fabsf(v));
                float rest = sum - pv;
                float pr = krl_eoo_phi(rest);
                if (pr < 0.0f) pr = 0.0f;
                m_cv[e] = krl_eoo_clampf((float)(inv_sign * sign) * pr, -KRL_EOO_LLR_MAX, KRL_EOO_LLR_MAX);
            }
        }

        /* --- variable-to-check 更新 --- */
        for (j = 0; j < KRL_EOO_CODE_BITS; j++) {
            int   nd = var_edge_cnt[j];
            float total = llr_ch[j];
            int   k;
            for (k = 0; k < nd; k++) total += m_cv[var_edges[j][k]];
            for (k = 0; k < nd; k++) {
                int e = var_edges[j][k];
                m_vc[e] = krl_eoo_clampf(total - m_cv[e], -KRL_EOO_LLR_MAX, KRL_EOO_LLR_MAX);
            }
        }

        /* --- 事後LLR・ハード判定 --- */
        for (j = 0; j < KRL_EOO_CODE_BITS; j++) {
            float L = llr_ch[j];
            int   k;
            for (k = 0; k < var_edge_cnt[j]; k++) L += m_cv[var_edges[j][k]];
            result.message[j] = (L < 0.0f) ? 1 : 0;
        }

        /* --- シンドローム検査: H * message^T == 0 (mod 2) --- */
        ok = 1;
        for (i = 0; i < KRL_EOO_NUM_CHECKS && ok; i++) {
            int ctr = 0;
            for (j = 0; j < KRL_EOO_CODE_BITS; j++) ctr += krl_eoo_hra[i][j] * result.message[j];
            if ((ctr % 2) != 0) ok = 0;
        }

        result.iterations = iter + 1;
        if (ok) { result.converged = 1; break; }
    }

    return result;
}

/* CRC-8 (生成多項式 0x1D, calculateCRC8_ in rade_text.cpp)。最初のNUL byteで停止 */
static uint8_t krl_eoo_crc8(const char* data, int maxlen)
{
    const uint8_t gen = 0x1D;
    uint8_t crc = 0x00;
    int i;
    for (i = 0; i < maxlen; i++) {
        uint8_t ch = (uint8_t)data[i];
        uint8_t acc;
        int b;
        if (ch == 0) break;
        acc = crc ^ ch;
        for (b = 0; b < 8; b++)
            acc = (acc & 0x80) ? (uint8_t)((acc << 1) ^ gen) : (uint8_t)(acc << 1);
        crc = acc;
    }
    return crc;
}

/* 6bit OTA値 → ASCII文字 (convert_ota_string_to_callsign_ in rade_text.cpp) */
static int krl_eoo_ota_to_ascii(const char* ota, int maxlen, char* out, int out_size)
{
    int i, n = 0;
    for (i = 0; i < maxlen && n < out_size - 1; i++) {
        uint8_t c = (uint8_t)ota[i];
        if (c == 0)             break;
        else if (c >= 1 && c <= 9) out[n++] = (char)(c + 37);
        else if (c >= 10 && c <= 19) out[n++] = (char)(c - 10 + '0');
        else if (c >= 20 && c <= 46) out[n++] = (char)(c - 20 + 'A');
        /* それ以外の値は OTA 文字セット外なので無視 */
    }
    out[n] = '\0';
    return n;
}

/* === RADE EOO コールサイン復号 (公開エントリポイント) ===
 *
 *   eoo_out : rade_rx() が出力した eoo_out[n_eoo] (180要素を想定)。
 *             rade_text_rx(textPtr, eooOut_, rade_n_eoo_bits(dv)/2) 相当。
 *   cs_out  : 復号成功時、NUL終端コールサイン文字列を書き込む
 *             (RADE_EOO_CALLSIGN_MAX+1 = 9 byte以上を確保すること)。
 *   conf_out: 0でなければ、デインターリーブ後56記号のRMS振幅を書き込む
 *             (-vv ログ等での参考表示用。LDPC復号自体には使用しない)。
 *
 *   戻り値: 1=成功 (LDPC収束 かつ CRC-8 一致), 0=失敗。
 */
int krl_eoo_decode_callsign(const float* eoo_out, int n_eoo,
    char* cs_out, int cs_out_size,
    float* conf_out)
{
    RADE_COMP in56[KRL_EOO_INFO_BITS];
    RADE_COMP pending[KRL_EOO_INFO_BITS];
    float     amps[KRL_EOO_INFO_BITS];
    krl_eoo_ldpc_result_t dec;
    float     rms = 0.0f;
    float     ss = 0.0f;
    int       ss_cnt = 0;
    float     noise_var;
    int       sym_size;
    char      raw[9];
    uint8_t   rx_crc, calc_crc;
    int       i, b;

    if (conf_out) *conf_out = 0.0f;
    if (cs_out_size > 0) cs_out[0] = '\0';

    /* 56記号(=112 float)が必要。残りはフィラー(雑音分散推定に使用) */
    if (n_eoo < KRL_EOO_CODE_BITS) return 0;
    sym_size = n_eoo / 2;

    /* (1) golden-prime デインターリーブ (先頭56記号)
     *     float[] → RADE_COMP[] は memcpy で安全にコピーする
     *     (strict-aliasing 違反を避けるため reinterpret しない) */
    memcpy(in56, eoo_out, sizeof(in56));
    for (i = 0; i < KRL_EOO_INFO_BITS; i++)
        pending[i] = in56[(KRL_EOO_INTERLEAVER_B * i) % KRL_EOO_INFO_BITS];

    /* (2) フィラー部 (56..sym_size-1 記号, 既知パターン=(1,0)) から
     *     雑音分散を実測する (deinterleaveしない元配列を直接参照) */
    for (i = KRL_EOO_INFO_BITS; i < sym_size; i++) {
        float re = eoo_out[2 * i];
        float im = eoo_out[2 * i + 1];
        float amp = sqrtf(re * re + im * im);
        if (amp > 0.0f) {
            float er = 1.0f - re / amp;
            float ei = 0.0f - im / amp;
            ss += er * er + ei * ei;
            ss_cnt += 2;
        }
    }
    noise_var = (ss_cnt > 1) ? (ss / (float)(ss_cnt - 1)) : 1.0f;
    if (noise_var < 1e-6f) noise_var = 1e-6f;

    /* (3) RMS (参考表示用。LDPC復号には使用しない) と各記号の振幅正規化 */
    for (i = 0; i < KRL_EOO_INFO_BITS; i++)
        rms += pending[i].real * pending[i].real + pending[i].imag * pending[i].imag;
    rms = sqrtf(rms / (float)sym_size);
    if (conf_out) *conf_out = rms;

    for (i = 0; i < KRL_EOO_INFO_BITS; i++) {
        float amp = sqrtf(pending[i].real * pending[i].real + pending[i].imag * pending[i].imag);
        if (amp < 1e-10f) {
            pending[i].real = 0.0f;
            pending[i].imag = 0.0f;
            amp = 1e-10f;
        }
        else {
            pending[i].real /= amp;
            pending[i].imag /= amp;
        }
        amps[i] = amp;
    }

    /* (4) LDPC(112,56) sum-product 復号 (シンドローム収束で成否判定) */
    dec = krl_eoo_ldpc_decode(pending, amps, noise_var, KRL_EOO_MAX_ITER);
    if (!dec.converged) return 0;

    /* (5) 56情報bit → 9byte (raw[0]=CRC-8, raw[1..8]=6bit OTA文字×8) */
    memset(raw, 0, sizeof(raw));
    for (b = 0; b < 8; b++)
        if (dec.message[b]) raw[0] |= (char)(1 << b);
    for (b = 8; b < KRL_EOO_INFO_BITS; b++) {
        int off = b - 8;
        if (dec.message[b]) raw[1 + off / 6] |= (char)(1 << (off % 6));
    }

    /* (6) CRC-8 検査 (OTAバイト列に対して計算、ASCIIではない) */
    rx_crc = (uint8_t)raw[0];
    calc_crc = krl_eoo_crc8(raw + 1, 8);
    if (rx_crc != calc_crc) return 0;

    /* (7) 6bit OTA → ASCII コールサイン
     *     注: 全0符号語は H・c=0 を自明に満たし、CRC-8(空文字列)=0x00 でもあるため
     *     「収束 かつ CRC一致」だけでは偽陽性(空コールサイン)を排除できない。
     *     空文字列はコールサインとして無意味なので失敗扱いとする。 */
    if (krl_eoo_ota_to_ascii(raw + 1, 8, cs_out, cs_out_size) == 0) return 0;
    return 1;
}

/*---------------------------------------------------------------------------
 * [SEC 6.6] RADE End-of-Over コールサイン エンコーダ
 *           (SEC 6.5 krl_eoo_decode_callsign の厳密な逆写像)
 *
 *   本家(freedv-backend / rade_text_generate_tx_string)と相互運用するための
 *   送信側。クリーンルーム実装で本家コードはリンクしない。
 *
 *   生成行列は krl_eoo_hra(H)から実行時に導出する(転記ミス排除):
 *     H = [H_A(56x56, 列0..55) | H_B(56x56, 列56..111)]、符号は列0..55が systematic。
 *     H_A·u ⊕ H_B·p = 0  →  p = H_B^{-1}·(H_A·u)  (GF(2))
 *   これで codeword c=[u|p]、H·c=0。RXは c[0..55] を info として読む。
 *
 *   出力 eoo_out[n_eoo] (=180, IQIQI): 先頭56記号=golden-prime(b=37)で
 *   インターリーブした符号語記号、残り=フィラー(1,0)。記号は軸上4点:
 *     bits(b0,b1): 00->(1,0) 01->(0,1) 10->(0,-1) 11->(-1,0)  (RXと同一)
 *   インターリーブ: RX pending[i]=in56[(37*i)%56] の逆 →
 *                   out_sym[(37*i)%56] = codeword_symbol[i]
 *
 *   ※ 本ファイルは krl_eoo_hra / krl_eoo_crc8 / KRL_EOO_* / RADE_COMP /
 *      krl_eoo_decode_callsign と同一翻訳単位に置くこと(SEC 6.5 の直後)。
 *-------------------------------------------------------------------------*/

/* ASCII -> 6bit OTA (krl_eoo_ota_to_ascii の逆)。書けた文字数を返す(最大8)。
 * '/'(47) 等 6bit OTA に無い文字はスキップ(本家も送出不可)。 */
static int krl_eoo_ascii_to_ota(const char *cs, unsigned char *ota8)
{
    int n = 0;
    const char *p;
    for (p = cs; *p && n < 8; ++p) {
        int o = (unsigned char)*p;
        if (o >= 'a' && o <= 'z') o -= 32;                 /* 大文字化 */
        if      (o >= '0' && o <= '9') ota8[n++] = (unsigned char)(o - '0' + 10);
        else if (o >= 'A' && o <= 'Z') ota8[n++] = (unsigned char)(o - 'A' + 20);
        else if (o >= 38 && o <= 46)   ota8[n++] = (unsigned char)(o - 37);   /* 記号帯 */
        /* それ以外はスキップ */
    }
    return n;
}

/* H_B (列56..111) の GF(2) 逆行列を一度だけ構築 */
static unsigned char krl_eoo_HBinv[KRL_EOO_INFO_BITS][KRL_EOO_INFO_BITS];
static int           krl_eoo_HBinv_ready = 0;

static int krl_eoo_build_HBinv(void)
{
    const int N = KRL_EOO_INFO_BITS;   /* 56 */
    unsigned char A[KRL_EOO_INFO_BITS][KRL_EOO_INFO_BITS];
    unsigned char I[KRL_EOO_INFO_BITS][KRL_EOO_INFO_BITS];
    int r, c, k;

    for (r = 0; r < N; r++)
        for (c = 0; c < N; c++) {
            A[r][c] = krl_eoo_hra[r][KRL_EOO_INFO_BITS + c];   /* H_B */
            I[r][c] = (r == c) ? 1 : 0;
        }

    for (c = 0; c < N; c++) {
        int piv = -1;
        for (r = c; r < N; r++) if (A[r][c]) { piv = r; break; }
        if (piv < 0) return -1;                                /* 特異(想定外) */
        if (piv != c)
            for (k = 0; k < N; k++) {
                unsigned char t;
                t = A[c][k]; A[c][k] = A[piv][k]; A[piv][k] = t;
                t = I[c][k]; I[c][k] = I[piv][k]; I[piv][k] = t;
            }
        for (r = 0; r < N; r++)
            if (r != c && A[r][c])
                for (k = 0; k < N; k++) { A[r][k] ^= A[c][k]; I[r][k] ^= I[c][k]; }
    }
    memcpy(krl_eoo_HBinv, I, sizeof(I));
    krl_eoo_HBinv_ready = 1;
    return 0;
}

/* callsign -> EOO 記号列 eoo_out[n_eoo] (float, IQIQI)。成功1/失敗0。 */
int krl_eoo_encode_callsign(const char *callsign, float *eoo_out, int n_eoo)
{
    unsigned char ota8[8];
    unsigned char raw[9];
    unsigned char msg[KRL_EOO_CODE_BITS];
    unsigned char Hu[KRL_EOO_INFO_BITS];
    int i, b, off, sym_size;

    if (!callsign || !eoo_out || n_eoo < KRL_EOO_CODE_BITS) return 0;
    if (!krl_eoo_HBinv_ready && krl_eoo_build_HBinv() != 0) return 0;

    /* (1) ASCII -> 6bit OTA、raw[0]=CRC-8(OTAバイト列), raw[1..8]=OTA */
    memset(ota8, 0, sizeof(ota8));
    krl_eoo_ascii_to_ota(callsign, ota8);
    memset(raw, 0, sizeof(raw));
    for (i = 0; i < 8; i++) raw[1 + i] = ota8[i];
    raw[0] = krl_eoo_crc8((const char *)(raw + 1), 8);

    /* (2) 56 info bits (LSB-first): [0..7]=CRC, [8..55]=6bit×8 */
    memset(msg, 0, sizeof(msg));
    for (b = 0; b < 8; b++) if ((raw[0] >> b) & 1) msg[b] = 1;
    for (off = 0; off < 48; off++) {
        int ci = off / 6, bit = off % 6;
        if ((raw[1 + ci] >> bit) & 1) msg[8 + off] = 1;
    }

    /* (3) parity = H_B^{-1}(H_A u) → codeword[56..111] */
    for (i = 0; i < KRL_EOO_INFO_BITS; i++) {
        int acc = 0, j;
        for (j = 0; j < KRL_EOO_INFO_BITS; j++) acc ^= (krl_eoo_hra[i][j] & msg[j]);
        Hu[i] = (unsigned char)acc;
    }
    for (i = 0; i < KRL_EOO_INFO_BITS; i++) {
        int acc = 0, j;
        for (j = 0; j < KRL_EOO_INFO_BITS; j++) acc ^= (krl_eoo_HBinv[i][j] & Hu[j]);
        msg[KRL_EOO_INFO_BITS + i] = (unsigned char)acc;
    }

    /* (4) フィラー(1,0)で全記号を初期化 */
    sym_size = n_eoo / 2;
    for (i = 0; i < sym_size; i++) { eoo_out[2 * i] = 1.0f; eoo_out[2 * i + 1] = 0.0f; }

    /* (5) 112bit → 56記号(軸上4点)→ golden-prime(b=37)で配置 */
    for (i = 0; i < KRL_EOO_INFO_BITS; i++) {
        int b0 = msg[2 * i], b1 = msg[2 * i + 1];
        float re, im;
        int pos;
        if      (!b0 && !b1) { re = 1.0f;  im = 0.0f; }
        else if (!b0 &&  b1) { re = 0.0f;  im = 1.0f; }
        else if ( b0 && !b1) { re = 0.0f;  im = -1.0f; }
        else                 { re = -1.0f; im = 0.0f; }
        pos = (KRL_EOO_INTERLEAVER_B * i) % KRL_EOO_INFO_BITS;   /* (37*i)%56 */
        eoo_out[2 * pos]     = re;
        eoo_out[2 * pos + 1] = im;
    }
    return 1;
}

/* 起動時セルフテスト: 既知コールサインを encode→decode して往復一致を確認。
 * 実機の krl_eoo_hra で検証されるので、生成行列導出/ビット詰めのミスをここで検出できる。
 * 戻り値: 全PASSで1。 */
int krl_eoo_selftest(void)
{
    static const char *tv[] = { "JH0VEQ", "JK2QQB", "JA1UQI", "W1AW", "8J1ABC" };
    const int N_EOO = 180;                 /* rade_n_eoo_bits(V1) */
    float eoo[180];
    char  cs[RADE_EOO_CALLSIGN_MAX + 1];
    int   t, ok_all = 1;
    for (t = 0; t < (int)(sizeof(tv) / sizeof(tv[0])); t++) {
        if (!krl_eoo_encode_callsign(tv[t], eoo, N_EOO)) { ok_all = 0; continue; }
        if (!krl_eoo_decode_callsign(eoo, N_EOO, cs, sizeof(cs), NULL)) { ok_all = 0; continue; }
        if (strcmp(cs, tv[t]) != 0) ok_all = 0;
    }
    return ok_all;
}
